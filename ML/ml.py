def compute_physics_features(df, elevation_m, slope, river_name="",
                              seasonal_rain_mean=None):
    """
    Converts raw weather data into physics-based flood features.

    New in this version:
      - Removed hard seasonal binary flags (is_monsoon etc.)
      - Added percentile-rank features that are month-agnostic
      - climate_shock_idx fires on extreme physical conditions
        regardless of calendar month
    """
    df = df.copy().sort_index()

    # ── Elevation physics ──────────────────────────────────────
    df['elevation_m']    = elevation_m
    df['low_elev_score'] = 1.0 / (1.0 + elevation_m / 15.0)

    # ── Slope ──────────────────────────────────────────────────
    df['slope']          = slope
    df['flat_land_score']= 1.0 / (1.0 + slope * 5000)

    # ── Flood Susceptibility Index (FSI) ──────────────────────
    # Pure physics — works for any location, any season
    df['flood_susceptibility_idx'] = (df['low_elev_score']
                                      * df['flat_land_score'])

    # ── Soil moisture ──────────────────────────────────────────
    if 'soil_moisture' not in df.columns:
        df['soil_moisture'] = 0.5
    df['soil_moisture'] = (df['soil_moisture']
                           .fillna(method='ffill').fillna(0.5))

    # ── Antecedent Precipitation Index ────────────────────────
    weights   = np.array([0.5**i for i in range(30)])
    rain_vals = df['rainfall_mm'].fillna(0).values
    api_vals  = np.zeros(len(df))
    for i in range(1, len(df)):
        window      = rain_vals[max(0, i-30):i][::-1]
        w           = weights[:len(window)]
        api_vals[i] = np.dot(window, w)
    df['antecedent_precip_idx'] = api_vals

    # ── Saturation index ──────────────────────────────────────
    rain_14d = df['rainfall_mm'].fillna(0).rolling(14).sum()
    df['saturation_idx'] = (
        df['soil_moisture'] * 0.6
        + (rain_14d / (rain_14d.max() + 1)) * 0.4
    ).clip(0, 1).fillna(0)

    # ── Rainfall anomaly ──────────────────────────────────────
    # today ÷ monthly mean — still useful but now supplemented
    # by month-agnostic percentile features
    if seasonal_rain_mean is not None and seasonal_rain_mean > 0:
        df['rain_anomaly'] = df['rainfall_mm'] / seasonal_rain_mean
    else:
        monthly_mean       = df['rainfall_mm'].groupby(
            df.index.month).transform('mean')
        df['rain_anomaly'] = df['rainfall_mm'] / (monthly_mean + 1)
    df['rain_anomaly'] = df['rain_anomaly'].fillna(1.0).clip(0, 20)

    # ── Rolling windows ───────────────────────────────────────
    for w in [3, 5, 7, 14, 30]:
        df[f'rain_{w}d_sum']  = df['rainfall_mm'].rolling(w).sum()
        df[f'rain_{w}d_max']  = df['rainfall_mm'].rolling(w).max()
        df[f'rain_{w}d_mean'] = df['rainfall_mm'].rolling(w).mean()

    # ── Lag features ──────────────────────────────────────────
    for lag in [1, 2, 3, 5, 7, 10, 14]:
        df[f'rain_lag{lag}d'] = df['rainfall_mm'].shift(lag)
        df[f'wl_lag{lag}d']   = df['water_level_m'].shift(lag)

    # ── Water level derived ───────────────────────────────────
    df['wl_7d_mean']   = df['water_level_m'].rolling(7).mean()
    df['wl_7d_std']    = df['water_level_m'].rolling(7).std().fillna(0)
    df['wl_trend_7d']  = df['water_level_m'] - df['water_level_m'].shift(7)
    df['wl_change_1d'] = df['water_level_m'].diff(1)
    df['wl_change_3d'] = df['water_level_m'].diff(3)
    df['wl_3d_max']    = df['water_level_m'].rolling(3).max()

    # ── Soft seasonal encoding (continuous, kept) ─────────────
    # month_sin/cos are continuous — they give the model a
    # smooth seasonal signal without hard blocking.
    # doy_sin/cos give finer within-year resolution.
    df['month']    = df.index.month
    df['doy']      = df.index.dayofyear
    df['month_sin']= np.sin(2 * np.pi * df['month'] / 12)
    df['month_cos']= np.cos(2 * np.pi * df['month'] / 12)
    df['doy_sin']  = np.sin(2 * np.pi * df['doy'] / 365)
    df['doy_cos']  = np.cos(2 * np.pi * df['doy'] / 365)

    # REMOVED: is_monsoon, is_premonsoon, is_postmonsoon
    # These hard binary flags blocked the model from responding
    # to early-monsoon or out-of-season extreme events.

    # ── Days from monsoon peak (continuous, not binary) ───────
    # Historical monsoon peak ≈ August 1 = doy 213.
    # This gives the model a smooth "how far from peak monsoon"
    # signal. It is large in winter, small in summer.
    # Critically: it is CONTINUOUS — an early June event
    # (doy 152) sits at distance 61 from peak, while August
    # (doy 213) sits at distance 0. The model can learn
    # "even at distance 61, FSI=0.9 means danger" without a
    # hard gate blocking it.
    doy_vals = df['doy'].values.astype(float)
    raw_dist = np.abs(doy_vals - 213)
    df['days_from_monsoon_peak'] = np.minimum(raw_dist,
                                               365 - raw_dist)

    # ── Temperature range ─────────────────────────────────────
    df['temp_range'] = (df.get('temp_max', pd.Series(30, index=df.index))
                        - df.get('temp_min', pd.Series(20, index=df.index)))

    # ── CLIMATE-AGNOSTIC PERCENTILE FEATURES ──────────────────
    # These three features are the core of the seasonal-blocking
    # fix. They compare each day against the ENTIRE historical
    # distribution — not against other days in the same month.

    # pct_rain_7d
    # Where does today's 7-day rain total rank among ALL days?
    # 0.95 = top 5% of all days ever — regardless of month.
    # An extreme May rainfall event ranks identically to an
    # equivalent August event.
    rain7 = df['rain_7d_sum'].fillna(0)
    df['pct_rain_7d'] = rain7.rank(pct=True, method='average')

    # pct_api
    # Where does today's soil saturation (API) rank overall?
    api_s = pd.Series(api_vals, index=df.index)
    df['pct_api'] = api_s.rank(pct=True, method='average')

    # pct_water_level
    # Where does today's river level rank overall?
    df['pct_water_level'] = (df['water_level_m']
                             .rank(pct=True, method='average'))

    # climate_shock_idx
    # Composite month-agnostic danger signal.
    # Weights:
    #   0.35 × rainfall intensity (7-day rank)
    #   0.35 × river level (rank)
    #   0.20 × soil saturation (API rank)
    #   0.10 × FSI (physical vulnerability — static per location)
    # A value near 1.0 means all indicators are simultaneously
    # at historical extremes — the model MUST flag this as
    # dangerous regardless of the calendar month.
    df['climate_shock_idx'] = (
        0.35 * df['pct_rain_7d']
      + 0.35 * df['pct_water_level']
      + 0.20 * df['pct_api']
      + 0.10 * df['flood_susceptibility_idx'].clip(0, 1)
    )

    # ── Final cleanup ─────────────────────────────────────────
    df = df.dropna(subset=['rainfall_mm', 'water_level_m'])
    df = df.fillna(method='ffill').fillna(0)
    return df

def compute_dynamic_risk_score(df_today, df_history):
    """
    Computes a 0-1 flood risk score from current conditions
    compared to seasonal norms. No static labels.

    Parameters:
        df_today   : single-row DataFrame for the date being assessed
        df_history : full historical DataFrame for the same station
                     (used to compute seasonal percentiles)
    Returns:
        float in [0, 1]
    """
    today_month = df_today.index[0].month

    # Seasonal subset for percentile calculation
    same_month_hist = df_history[df_history.index.month == today_month]
    if len(same_month_hist) < 10:
        same_month_hist = df_history   # fall back to full history if too few

    def safe_pct(series, pct):
        v = series.dropna()
        return float(np.percentile(v, pct)) if len(v) > 0 else 1.0

    wl_median  = safe_pct(same_month_hist['water_level_m'], 50)
    rain7_p90  = safe_pct(same_month_hist.get('rain_7d_sum',
                   same_month_hist['rainfall_mm'].rolling(7).sum()), 90)
    api_p90    = safe_pct(same_month_hist.get('antecedent_precip_idx',
                   pd.Series([1])), 90)

    # Component 1: Water level anomaly
    wl_now  = float(df_today['water_level_m'].iloc[0])
    c1      = np.clip(wl_now / (wl_median + 0.01) - 0.5, 0, 3) / 3.0

    # Component 2: Rainfall intensity
    rain7_now = float(df_today.get('rain_7d_sum', df_today['rainfall_mm']).iloc[0])
    c2        = np.clip(rain7_now / (rain7_p90 + 0.01), 0, 2) / 2.0

    # Component 3: Soil/catchment saturation
    api_now = float(df_today.get('antecedent_precip_idx', pd.Series([0])).iloc[0])
    c3      = np.clip(api_now / (api_p90 + 0.01), 0, 1)

    # Component 4: Upstream pressure (using available upstream lag features)
    upstream_cols = [c for c in df_today.columns if c.startswith('up_')]
    if upstream_cols:
        up_vals = df_today[upstream_cols].iloc[0].values
        c4      = np.clip(float(up_vals.mean()) / 50.0, 0, 1)
    else:
        c4 = c2 * 0.5   # proxy from local rain if no upstream data

    score = 0.40 * c1 + 0.30 * c2 + 0.20 * c3 + 0.10 * c4
    return float(np.clip(score, 0, 1))


def risk_level_from_score(score):
    """Converts 0-1 score to human-readable level and colour code."""
    if score < 0.20:   return "SAFE",    "\033[92m",  "green"
    if score < 0.45:   return "WATCH",   "\033[93m",  "yellow"
    if score < 0.68:   return "WARNING", "\033[33m",  "orange"
    if score < 0.85:   return "DANGER",  "\033[91m",  "red"
    return                    "EXTREME", "\033[41m",  "darkred"

def fetch_weather(lat, lon, start_date, end_date, name=""):
    """Calls Open-Meteo Archive."""

    try:
        # --- Fetch daily data ---
        daily_params = {
            'latitude': lat, 'longitude': lon,
            'start_date': start_date, 'end_date': end_date,
            'daily': ['precipitation_sum', 'temperature_2m_max',
                      'temperature_2m_min', 'wind_speed_10m_max',
                      'relative_humidity_2m'],
            'timezone': 'Asia/Dhaka',
        }
        r_daily = requests.get(METEO_ARCHIVE, params=daily_params, timeout=45)
        r_daily.raise_for_status()
        daily_data = r_daily.json()['daily']

        df = pd.DataFrame({
            'date':             pd.to_datetime(daily_data['time']),
            'rainfall_mm':      daily_data['precipitation_sum'],
            'temp_max':         daily_data['temperature_2m_max'],
            'temp_min':         daily_data['temperature_2m_min'],
            'wind_speed':       daily_data['wind_speed_10m_max'],
            'evapotranspiration': np.nan, # Removed from API call, set to NaN
            'humidity':         daily_data['relative_humidity_2m'],
        })
        df['station'] = name
        df = df.set_index('date')

        # --- Fetch hourly soil moisture ---
        hourly_params = {
            'latitude': lat, 'longitude': lon,
            'start_date': start_date, 'end_date': end_date,
            'hourly': 'soil_moisture_0_to_7cm',
            'timezone': 'Asia/Dhaka',
        }
        r_hourly = requests.get(METEO_ARCHIVE, params=hourly_params, timeout=45)
        r_hourly.raise_for_status()
        hourly_data = r_hourly.json()['hourly']

        h = pd.DataFrame({'dt': pd.to_datetime(hourly_data['time']),
                           'sm': hourly_data['soil_moisture_0_to_7cm']})
        h['date'] = h['dt'].dt.normalize()
        hourly_mean_sm = h.groupby('date')['sm'].mean().rename('soil_moisture')
        df = df.merge(hourly_mean_sm, on='date', how='left')

        return df

    except Exception as e:
        print(f"  [WEATHER WARN] {name}: {e}")
        return None

