def crop_to_match(tensor, target):
    """
    Crops `tensor` so its spatial dimensions match `target`.

    Why this exists:
      After two rounds of MaxPooling2D(2) + Conv2DTranspose(2),
      integer rounding means the upsampled shape can be off by
      ±1 pixel vs the skip connection it needs to concatenate with.
      This function trims the extra row or column so Concatenate
      never sees a mismatch — regardless of grid dimensions.

    With GRID_ROWS=24 and GRID_COLS=24 this function does nothing
    (both tensors are already the same size). It is kept as a
    safety net in case the grid dimensions are changed later.
    """
    ts = tf.shape(tensor)
    tg = tf.shape(target)
    # Crop height if needed
    h  = tf.minimum(ts[1], tg[1])
    # Crop width if needed
    w  = tf.minimum(ts[2], tg[2])
    return tensor[:, :h, :w, :]


def build_unet(shape=(GRID_ROWS, GRID_COLS, N_CHANNELS)):
    """
    U-Net encoder–decoder with skip connections.

    Shape check before building:
      Both spatial dimensions must be divisible by 4 for clean
      round-trip pooling. This is now guaranteed by GRID_COLS=24.

    Architecture:
      Encoder block 1: Conv→BN→Conv → c1 (skip)  → Pool → p1
      Encoder block 2: Conv→BN→Conv → c2 (skip)  → Pool → p2
      Bottleneck     : Conv→BN→Conv → bn
      Decoder block 1: Upsample → crop_to_match(c2) → Concat → Conv→Conv
      Decoder block 2: Upsample → crop_to_match(c1) → Concat → Conv→Conv
      Output         : Conv2D(1, sigmoid) → flood probability per pixel
    """
    rows, cols, _ = shape
    assert rows % 4 == 0, f"GRID_ROWS ({rows}) must be divisible by 4"
    assert cols % 4 == 0, f"GRID_COLS ({cols}) must be divisible by 4"

    inp = keras.Input(shape=shape)

    # ── Encoder ──
    c1 = layers.Conv2D(32, 3, activation='relu', padding='same')(inp)
    c1 = layers.BatchNormalization()(c1)
    c1 = layers.Conv2D(32, 3, activation='relu', padding='same')(c1)
    p1 = layers.MaxPooling2D(2)(c1)
    p1 = layers.Dropout(0.10)(p1)

    c2 = layers.Conv2D(64, 3, activation='relu', padding='same')(p1)
    c2 = layers.BatchNormalization()(c2)
    c2 = layers.Conv2D(64, 3, activation='relu', padding='same')(c2)
    p2 = layers.MaxPooling2D(2)(c2)
    p2 = layers.Dropout(0.10)(p2)

    # ── Bottleneck ──
    bn = layers.Conv2D(128, 3, activation='relu', padding='same')(p2)
    bn = layers.BatchNormalization()(bn)
    bn = layers.Conv2D(128, 3, activation='relu', padding='same')(bn)

    # ── Decoder ──
    u4 = layers.Conv2DTranspose(64, 2, strides=2, padding='same')(bn)
    # crop_to_match ensures shapes agree even if rounding produces ±1 diff
    u4 = layers.Lambda(lambda t: crop_to_match(t[0], t[1]))([u4, c2])
    u4 = layers.Concatenate()([u4, c2])
    c4 = layers.Conv2D(64, 3, activation='relu', padding='same')(u4)
    c4 = layers.Conv2D(64, 3, activation='relu', padding='same')(c4)

    u5 = layers.Conv2DTranspose(32, 2, strides=2, padding='same')(c4)
    u5 = layers.Lambda(lambda t: crop_to_match(t[0], t[1]))([u5, c1])
    u5 = layers.Concatenate()([u5, c1])
    c5 = layers.Conv2D(32, 3, activation='relu', padding='same')(u5)
    c5 = layers.Conv2D(32, 3, activation='relu', padding='same')(c5)

    # ── Output — one flood probability per pixel ──
    out = layers.Conv2D(1, 1, activation='sigmoid')(c5)

    m = keras.Model(inp, out)
    m.compile(
        optimizer=keras.optimizers.Adam(1e-3),
        loss='binary_crossentropy',
        metrics=['accuracy', tf.keras.metrics.AUC(name='auc')],
    )
    return m


