def compute_slope(stations):
    if len(stations) < 3:
        for s in stations:
            s.setdefault('slope', 0.0001)
        return stations
    lats  = np.array([s['lat']                   for s in stations])
    lons  = np.array([s['lon']                   for s in stations])
    elevs = np.array([s.get('elevation_m', 10.0) for s in stations])
    for i, s in enumerate(stations):
        dists    = np.sqrt((lats - s['lat'])**2 + (lons - s['lon'])**2)
        dists[i] = 999.0
        nn       = np.argsort(dists)[:4]
        dist_m   = dists[nn] * 111_000 + 1.0
        s['slope'] = float(np.mean(
            np.abs(elevs[nn] - s.get('elevation_m', 10.0)) / dist_m))
    return stations


def simulate_water_level(df, elevation_m):
    """
    Physics-based water level from rainfall + elevation.
    85th-percentile danger threshold guarantees ~15% flood rate.
    """
    np.random.seed(abs(int(elevation_m * 100)) % (2**31 - 1))
    n        = len(df)
    doy      = df.index.dayofyear.values
    seasonal = 2.0 + 1.8 * np.sin(2 * np.pi * (doy - 80) / 365)
    rain     = df['rainfall_mm'].fillna(0).values
    lagged   = np.zeros(n)
    for lag, w in [(2, 0.35), (3, 0.40), (4, 0.15), (5, 0.10)]:
        pad     = np.pad(rain, (lag, 0), mode='constant')[:n]
        lagged += w * pad
    mx = lagged.max()
    if mx > 0:
        lagged = (lagged / mx) * (5.0 / (1.0 + elevation_m / 10.0))
    water_level   = seasonal + lagged + np.random.normal(0, 0.15, n)
    danger_thresh = float(np.percentile(water_level, 85))
    df = df.copy()
    df['water_level_m']  = water_level
    df['danger_thresh']  = danger_thresh
    df['flood_occurred'] = (water_level > danger_thresh).astype(int)
    df['flood_severity'] = np.clip(
        np.digitize(water_level,
                    [0,
                     danger_thresh * 0.60,
                     danger_thresh * 0.85,
                     danger_thresh]) - 1, 0, 3)
    return df


