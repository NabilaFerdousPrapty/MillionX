def create_spatial_dataset(n=1200, rows=GRID_ROWS, cols=GRID_COLS,
                            ch=N_CHANNELS):
    """
    Generates synthetic Bangladesh spatial data for UNet training.

    Grid size is now 24×24 — both dimensions divisible by 4,
    so MaxPooling2D(2) twice and Conv2DTranspose(2) twice
    produce exact round-trip dimensions with no rounding.

    Channel layout (7 channels):
      0  Daily rainfall (mm)
      1  Elevation (m) from SRTM
      2  Terrain slope (derived from elevation gradient)
      3  Soil moisture (0–1)
      4  Distance to nearest river (0–1)
      5  Upstream India/Nepal rainfall — lagged 3 days
      6  Flood Susceptibility Index = (1/elev) × (1/slope)
    """
    np.random.seed(42)
    Xs, ys = [], []

    for _ in range(n):
        img = np.zeros((rows, cols, ch), dtype=np.float32)

        # Ch 0 — Rainfall
        rain          = np.random.exponential(25, (rows, cols)).astype(np.float32)
        rain[:8, 14:] *= 2.8    # Sylhet / Sunamganj: very high
        rain[8:16, :8]*= 1.4    # Brahmaputra corridor: elevated
        img[:, :, 0]   = np.clip(rain, 0, 220)

        # Ch 1 — Elevation
        elev              = np.zeros((rows, cols), dtype=np.float32)
        elev[16:, 16:]    = np.random.uniform(80, 900, (8, 8))   # Chittagong Hills
        elev[:6,  18:]    = np.random.uniform(30, 250, (6, 6))   # Sylhet hills
        elev[6:16, :16]   = np.random.uniform(0,   12, (10, 16)) # Main floodplain
        img[:, :, 1]      = elev

        # Ch 2 — Slope (gradient of elevation)
        gy, gx         = np.gradient(elev)
        slope_map      = np.sqrt(gx**2 + gy**2)
        img[:, :, 2]   = np.clip(slope_map, 0, 30)

        # Ch 3 — Soil moisture
        sm             = np.random.uniform(0.3, 0.85, (rows, cols)).astype(np.float32)
        sm[6:16, :16]  = np.random.uniform(0.65, 0.97, (10, 16))
        img[:, :, 3]   = sm

        # Ch 4 — Distance to nearest river
        rdist = np.ones((rows, cols), dtype=np.float32)
        for rc in [4, 7, 11, 16]:
            for c in range(cols):
                rdist[:, c] = np.minimum(rdist[:, c], abs(c - rc) / cols)
        img[:, :, 4] = rdist

        # Ch 5 — Upstream rainfall (lagged 3 days)
        up         = np.random.exponential(18, (rows, cols)).astype(np.float32)
        up[:6, :]  *= 2.1
        img[:, :, 5] = up

        # Ch 6 — Flood Susceptibility Index (physics formula)
        low_elev     = 1.0 / (1.0 + elev / 15.0)
        flat         = 1.0 / (1.0 + slope_map * 5000)
        img[:, :, 6] = (low_elev * flat).astype(np.float32)

        # Label — flood probability from physics (no historical labels)
        flood_prob = (
            low_elev          * 0.30 +
            (rain / 220)      * 0.25 +
            sm                * 0.20 +
            (1 - rdist)       * 0.15 +
            flat              * 0.10
        )
        flood_prob += np.random.normal(0, 0.04, (rows, cols))
        ys.append((flood_prob > 0.52).astype(np.float32))
        Xs.append(img)

    return np.array(Xs), np.array(ys)[..., np.newaxis]


