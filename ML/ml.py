def _date_to_nasa(date_str):
    """Converts '2015-01-01' → '20150101' for NASA POWER API."""
    return date_str.replace('-', '')


def fetch_nasa_power(lat, lon, start_date, end_date,
                     name="", elevation_m=10.0, max_retries=4):
    """
    Fetches daily weather data from NASA POWER API.

    NASA POWER advantages over Open-Meteo for this use case:
      - No hourly quota limit
      - Returns the entire date range in one request
      - No authentication needed
      - Reliable for research use (backed by NASA LaRC)

    Parameters
    ----------
    lat, lon      : station coordinates
    start_date    : "YYYY-MM-DD"
    end_date      : "YYYY-MM-DD"
    name          : label for the station (used in output)
    elevation_m   : passed in from elev_hint (NASA does not return elevation)
    max_retries   : retry attempts on network failure

    Returns
    -------
    (df, elevation_m)  on success — elevation is the passed-in value
    (None, 10.0)       on failure
    """
    # Check disk cache first — no API call if already fetched
    cached_df, cached_elev = load_from_cache(name)
    if cached_df is not None:
        return cached_df, cached_elev

    params = {
        "parameters": NASA_PARAMS,
        "community":  "AG",          # Agroclimatology — best for hydrology
        "longitude":  lon,
        "latitude":   lat,
        "start":      _date_to_nasa(start_date),
        "end":        _date_to_nasa(end_date),
        "format":     "JSON",
    }

    for attempt in range(1, max_retries + 1):
        try:
            r = requests.get(NASA_POWER_URL, params=params, timeout=120)

            # NASA returns 429 very rarely but handle it anyway
            if r.status_code == 429:
                wait = 30 * attempt
                print(f"  [429] NASA rate limit — waiting {wait}s ...")
                time.sleep(wait)
                continue

            # NASA sometimes returns 5xx on server load
            if r.status_code >= 500:
                wait = 20 * attempt
                print(f"  [{r.status_code}] NASA server busy — "
                      f"waiting {wait}s (attempt {attempt}) ...")
                time.sleep(wait)
                continue

            r.raise_for_status()
            data   = r.json()
            params_data = data['properties']['parameter']

            # Build date index from PRECTOTCORR keys (YYYYMMDD strings)
            date_keys = list(params_data['PRECTOTCORR'].keys())
            dates     = pd.to_datetime(date_keys, format='%Y%m%d')

            def _extract(key):
                """Gets values for a parameter, replacing -999 with NaN."""
                vals = [params_data[key].get(d, np.nan) for d in date_keys]
                arr  = np.array(vals, dtype=float)
                arr[arr <= -998] = np.nan
                return arr

            df = pd.DataFrame({
                'date':               dates,
                'rainfall_mm':        _extract('PRECTOTCORR'),
                'temp_max':           _extract('T2M_MAX'),
                'temp_min':           _extract('T2M_MIN'),
                'wind_speed':         _extract('WS2M'),
                'humidity':           _extract('RH2M'),
                'soil_moisture':      _extract('GWETROOT'),
                'evapotranspiration': _extract('EVPTRNS'),
            })

            df['station'] = name
            df = df.set_index('date').sort_index()

            # Forward-fill any remaining NaN (isolated missing days)
            df = df.fillna(method='ffill').fillna(method='bfill')

            # Save to disk immediately
            save_to_cache(name, df, elevation_m)
            return df, elevation_m

        except requests.exceptions.Timeout:
            print(f"  [TIMEOUT] {name} — attempt {attempt}/{max_retries} ...")
            time.sleep(15 * attempt)

        except requests.exceptions.ConnectionError as e:
            print(f"  [CONN ERR] {name}: {e}")
            time.sleep(10 * attempt)

        except KeyError as e:
            print(f"  [PARSE ERR] {name}: missing key {e} in NASA response")
            return None, elevation_m

        except Exception as e:
            print(f"  [ERR] {name}: {e}")
            return None, elevation_m

    print(f"  [SKIP] {name}: all {max_retries} retries exhausted.")
    return None, elevation_m


