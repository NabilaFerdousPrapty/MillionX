def temporal_sample_weights(index):
    """
    Returns a numpy array of per-sample weights based on year.
    Pass this to XGBoost's sample_weight and LSTM's sample_weight.

    Parameters
    ----------
    index : pd.DatetimeIndex
        The index of X_train (or y_train).

    Returns
    -------
    np.ndarray of float, same length as index.
    """
    year = pd.Series(index.year, index=index)
    w    = pd.Series(1.0, index=index)

    w[year < 1990]                          = 0.55
    w[(year >= 1990) & (year < 2000)]       = 0.70
    w[(year >= 2000) & (year < 2010)]       = 0.85
    w[year >= 2010]                         = 1.00

    return w.values


