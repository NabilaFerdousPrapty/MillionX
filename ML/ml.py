def predict_any_location(lat, lon, location_name=None, days_forecast=3):
    """
    Predicts flood risk for ANY latitude/longitude in Bangladesh.
    Works for areas with no flood history by using physics features.

    Steps:
      1. Fetch real SRTM elevation for the point (OpenTopoData)
      2. Estimate slope from nearby elevation points
      3. Fetch 50 days of weather history (NASA POWER)
      4. Fetch 3-7 day forecast (Open-Meteo Forecast)
      5. Build physics features (FSI, saturation, anomaly)
      6. Run XGBoost + LSTM + UNet
      7. Compute DYNAMIC risk score from current conditions
      8. Return result with risk level
    """
    if location_name is None:
        location_name = f"({lat:.3f}, {lon:.3f})"

    # Determine the correct name/label for caching and API calls
    actual_name = location_name
    matched_station_info = None
    # Iterate through all_stations to find a match and use its cached label
    # all_stations is a global variable populated by load_all_data()
    for st_info in all_stations:
        # Using a small tolerance for floating point comparisons
        if abs(st_info['lat'] - lat) < 0.001 and abs(st_info['lon'] - lon) < 0.001:
            actual_name = st_info['label']
            matched_station_info = st_info
            break

    print(f"\n[ REAL-TIME ] Predicting for: {location_name} (Internal label: {actual_name})")

    # Step 1 — get elevation for this exact coordinate
    try:
        r = requests.get(TOPO_URL, params={'locations': f'{lat},{lon}'}, timeout=15)
        elevation_m = r.json()['results'][0].get('elevation', 10.0)
    except:
        elevation_m = 10.0
        print("  Could not fetch elevation — using 10m default")

    # Estimate slope from 4 nearby points
    offsets = [(0.05, 0), (-0.05, 0), (0, 0.05), (0, -0.05)]
    try:
        locs = "|".join(f"{lat+dy},{lon+dx}" for dy, dx in offsets)
        r    = requests.get(TOPO_URL, params={'locations': locs}, timeout=15)
        nn_elevs = [x.get('elevation', elevation_m)
                    for x in r.json().get('results', [])]
        diffs    = [abs(e - elevation_m) for e in nn_elevs]
        slope    = float(np.mean(diffs)) / (0.05 * 111_000)
    except:
        slope = 0.001

    print(f"  Elevation: {elevation_m:.1f}m   Slope: {slope:.6f}")

    # Step 2 — fetch weather history using NASA POWER API
    # Ensure dates are in the past for archive API
    # Using VAL_END as the historical end date to be consistent with training data
    # and avoid issues with system date if it reports a future year.
    hist_en = VAL_END # This is '2023-12-31'
    hist_st = (pd.to_datetime(VAL_END) - timedelta(days=55)).strftime('%Y-%m-%d')

    # Pass the determined actual_name to fetch_nasa_power
    hist, _ = fetch_nasa_power(lat, lon, hist_st, hist_en, name=actual_name, elevation_m=elevation_m)
    if hist is None:
        print("  NASA POWER API unavailable for historical data. Cannot predict.")
        return None

    hist = simulate_water_level(hist, elevation_m)

    # Step 3 — physics features
    # If matched_station_info was found, use its pre-computed elevation and slope
    if matched_station_info:
        feat_df = compute_physics_features(hist,
                                           elevation_m=matched_station_info['elevation_m'],
                                           slope=matched_station_info['slope'])
    else:
        feat_df = compute_physics_features(hist, elevation_m, slope)

    for up_name, up_df in upstream_data.items():
        short = up_name.split('_')[0][:5]
        up_rain = up_df['rainfall_mm'].reindex(feat_df.index, method='nearest')
        for lag in [2, 3, 4, 5, 7]:
            feat_df[f'up_{short}_lag{lag}d'] = up_rain.shift(lag)
        feat_df[f'up_{short}_7d_sum'] = up_rain.rolling(7).sum()
    feat_df = feat_df.fillna(method='ffill').fillna(0)

    if len(feat_df) < SEQUENCE_LENGTH + 2:
        print(f"  Not enough history ({len(feat_df)} rows).")
        return None

    # Step 4 — XGBoost prediction
    today_row = feat_df[FEATURE_COLS].tail(1).reindex(
        columns=FEATURE_COLS, fill_value=0)
    xgb_p = float(xgb_model.predict_proba(today_row)[0, 1])

    # Step 5 — LSTM prediction
    seq   = feat_df[FEATURE_COLS].tail(SEQUENCE_LENGTH).values
    seq_s = scaler_lstm.transform(seq)[np.newaxis].astype(np.float32)
    lstm_p = float(lstm_model.predict(seq_s, verbose=0)[0, 0])

    # Step 6 — UNet spatial estimate
    sp_demo = create_spatial_dataset(n=1)[0][:1]
    sp_norm = (sp_demo - sp_mean) / sp_std
    flood_map = unet_model.predict(sp_norm, verbose=0)[0, :, :, 0]
    unet_p    = float(flood_map.mean())

    # Step 7 — DYNAMIC risk score (no static label)
    dynamic_score = compute_dynamic_risk_score(feat_df.tail(1), feat_df)

    # Hybrid model probability
    model_p = 0.40 * xgb_p + 0.40 * lstm_p + 0.20 * unet_p

    # Final score blends model output with dynamic physics score
    final_score = 0.65 * model_p + 0.35 * dynamic_score

    level, colour, _ = risk_level_from_score(final_score)
    rst = "\033[0m"

    # Step 8 — Forecast
    fcast = fetch_forecast(lat, lon, location_name, days=days_forecast)

    # Physics explanation for why this location is at risk
    fsi = float(feat_df['flood_susceptibility_idx'].iloc[-1])
    sat = float(feat_df['saturation_idx'].iloc[-1])
    anom = float(feat_df['rain_anomaly'].iloc[-1])

    print("\n" + "=" * 55)
    print(f"  FLOOD RISK — {location_name.upper()}")
    print("=" * 55)
    print(f"  RISK LEVEL     : {colour}[ {level} ]{rst}")
    print(f"  Final score    : {final_score:.1%}")
    print(f"  ├─ ML model    : {model_p:.1%}  (XGBoost + LSTM + UNet)")
    print(f"  └─ Physics     : {dynamic_score:.1%}  (live conditions vs seasonal norm)")
    print(f"\n  Physics breakdown:")
    print(f"  ├─ Elevation        : {elevation_m:.1f} m")
    print(f"  ├─ Flood susc. idx  : {fsi:.3f}  (0=safe, 1=very vulnerable)")
    print(f"  ├─ Soil saturation  : {sat:.3f}  (0=dry, 1=waterlogged)")
    print(f"  └─ Rainfall anomaly : {anom:.2f}x  (vs seasonal normal)")

    if fcast is not None:
        print(f"\n  Forecast (next {days_forecast} days):")
        for dt, row in fcast.head(days_forecast).iterrows():
            bar = '#' * max(0, int(row['rainfall_mm'] / 5))
            print(f"    {dt.strftime('%b %d')} : {row['rainfall_mm']:5.1f} mm  {bar}")

    return {
        'location': location_name, 'lat': lat, 'lon': lon,
        'elevation_m': elevation_m, 'slope': slope,
        'flood_susceptibility_idx': fsi,
        'risk_level': level, 'final_score': round(final_score, 4),
        'model_prob': round(model_p, 4),
        'dynamic_risk': round(dynamic_score, 4),
        'xgb_prob': round(xgb_p, 4), 'lstm_prob': round(lstm_p, 4),
        'unet_prob': round(unet_p, 4),
        'flood_map': flood_map, 'forecast': fcast,
    }

