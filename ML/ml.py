def fetch_forecast(lat, lon, name="", days=7):
    """
    Fetches next N-day forecast from Open-Meteo Forecast API.
    This is lightweight (7 days only) and has no quota issue.
    NASA POWER is historical-only so we keep this for predictions.
    """
    params = {
        'latitude':      lat,
        'longitude':     lon,
        'daily': [
            'precipitation_sum',
            'temperature_2m_max',
            'wind_speed_10m_max',
            'precipitation_probability_max',
        ],
        'forecast_days': days,
        'timezone':      'Asia/Dhaka',
    }
    for attempt in range(3):
        try:
            r = requests.get(METEO_FORECAST, params=params, timeout=30)
            if r.status_code == 429:
                time.sleep(10 * (2 ** attempt))
                continue
            r.raise_for_status()
            d  = r.json()['daily']
            n  = len(d['time'])
            df = pd.DataFrame({
                'date':            pd.to_datetime(d['time']),
                'rainfall_mm':     d['precipitation_sum'],
                'temp_max':        d['temperature_2m_max'],
                'wind_speed':      d['wind_speed_10m_max'],
                'rain_probability':d.get(
                    'precipitation_probability_max', [50]*n),
            })
            df['station'] = name
            return df.set_index('date')
        except Exception as e:
            print(f"  [FORECAST WARN] {name}: {e}")
    return None


